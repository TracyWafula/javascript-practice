//Full Name Builder

let firstName = "Racheal";
let secondName = "Makena";
let lastName = "Neema";

//print each one separately
console.log(`FirstName:${firstName}`);
console.log(`SecondtName:${secondName}`);
console.log(`LastName:${lastName}`);

//join them into one fullName

let fullName =  `${firstName} ${secondName} ${lastName}` 
console.log(`FullName ${fullName}`);

//print the length of the full name
console.log(`Fullname contains ${fullName.length} characters`);